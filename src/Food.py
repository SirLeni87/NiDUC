class Food():

    def __init__(self, x, y, amount):
        self.x = x
        self.y = y
        self.amount = amount

    def getX(self):
        return self.x

    def setX(self, x):
        self.x = x

    def getY(self):
        return self.y

    def setY(self, y):
        self.y = y

    def getAmount(self):
        return self.y

    def setAmount(self, amount):
        self.amount = amount
